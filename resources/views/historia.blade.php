                        <h2 class="text-2xl font-serif mb-4 text-justify border-b pb-2">6. Siglo XXI: Actualidad y retos</h2>
                        <p class="font-serif mb-4 text-justify">En la actualidad, Escuinapa sigue destacándose por:
                            Su economía agrícola y pesquera, con exportación de mango a países como Estados Unidos, Canadá y Japón.
                            Su creciente interés en el turismo local, gracias a playas como Teacapán, Las Cabras y La Tambora, y a sus tradiciones populares.
                            También enfrenta retos importantes como:
                            Falta de infraestructura médica avanzada.
                            Vulnerabilidad ante huracanes y fenómenos naturales, como lo fue el huracán Willa en 2018.
                            Necesidad de modernización educativa y tecnológica, especialmente en zonas rurales</p>
