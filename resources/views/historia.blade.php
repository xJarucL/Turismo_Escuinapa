                        <h2 class="text-2xl text-justify font-serif mb-4 border-b pb-2">7. Cultura y tradiciones</h2>
                        <p class="font-serif mb-4 text-justify">Entre las más destacadas están:
                            Fiesta del Mar de las Cabras: Celebración tradicional en las playas cercanas, donde miles de personas acampan, realizan procesiones religiosas, concursos, y conviven en familia.
                            Carnavales con desfiles, reinas y danzas.
                            Música tradicional como el banda sinaloense, corridos y música ranchera.
                            Gastronomía: Basada en mariscos, como ceviches, aguachiles, tacos de camarón, tamales barbones (hechos con camarón con todo y cabeza), y dulces de mango.</p>
