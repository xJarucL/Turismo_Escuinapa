                        <h2 class="text-2xl text-justify font-serif mb-4 border-b pb-2">2. Conquista española y evangelización (siglos XVI-XVII)</h2>
                        <p class="font-serif mb-4 text-justify">En 1531, los españoles, liderados por Nuño de Guzmán, iniciaron la conquista del actual territorio de Sinaloa. La región de Escuinapa fue explorada más adelante por los misioneros franciscanos y jesuitas, quienes buscaron evangelizar a los pueblos originarios.</p>
                        <p class="font-serif mb-4 text-justify">Los religiosos fundaron misiones en la zona y enseñaron nuevas formas de cultivo, lengua española y religión católica. Los indígenas fueron agrupados en pueblos de misión. Esto transformó profundamente su modo de vida. Entre las misiones cercanas destacadas está la de Chametla, que tuvo gran influencia sobre Escuinapa.</p>
                    </div>
                </div>

                <div class="mb-10 grid grid-cols-1 md:grid-cols-2 gap-8">
                    <div>
                        <h2 class="text-2xl font-serif mb-4 border-b pb-2 text-justify">3. Período virreinal (siglos XVII-XVIII)</h2>
                        <p class="font-serif mb-4 text-justify">Durante el Virreinato de la Nueva España, Escuinapa fue parte de la Provincia de Culiacán. Aunque no fue un centro importante en términos políticos, sí lo fue como paso comercial y agrícola. Los caminos reales conectaban los pueblos costeros con el interior del país, y los productos como sal, pescado seco, maíz y frijol se intercambiaban o enviaban hacia otras regiones.</p>
                        <p class="font-serif mb-4 text-justify">La población creció lentamente, y se fortalecieron las haciendas que controlaban la producción agrícola y ganadera. Las relaciones entre los colonos y los indígenas fueron muchas veces tensas, con levantamientos ocasionales.</p>
                    </div>

                    <div>
                        <h2 class="text-2xl font-serif mb-4 text-justify border-b pb-2">4. Siglo XIX: Independencia y vida republicana</h2>
                        <p class="font-serif mb-4 text-justify">Tras la Independencia de México en 1821, la región de Escuinapa se incorporó al nuevo estado mexicano. A mediados del siglo XIX, formó parte del Distrito de Rosario, y su importancia fue creciendo poco a poco por su producción agrícola y ganadera.</p>
                        <p class="font-serif mb-4 text-justify">Durante la Reforma y el Porfiriato, hubo mejoras en caminos, comercio y transporte. Se comenzó a construir infraestructura básica y aumentó la actividad pesquera. En 1880, Escuinapa fue elevada a la categoría de villa, y en 1915, en el marco de la Revolución Mexicana, fue declarada oficialmente municipio libre, con la cabecera municipal Escuinapa de Hidalgo.</p>
                    </div>
                </div>


                <div class="mb-10 grid grid-cols-1 md:grid-cols-2 gap-8 items-center">
                    <div>
                        <h2 class="text-2xl font-serif mb-4 text-justify border-b pb-2">5. Siglo XX: Crecimiento económico y social</h2>
                        <p class="font-serif mb-4 text-justify">Durante el siglo XX, Escuinapa vivió un proceso de modernización en varias áreas:
                            Agricultura: Se convirtió en uno de los mayores productores de mango ataulfo, papaya, plátano y tomate.
                            Pesca: El camarón es el principal producto del mar. El municipio posee una de las flotas camaroneras más importantes del sur de Sinaloa, y cuenta con esteros, marismas y campos pesqueros relevantes.
                            Infraestructura: Se construyeron caminos, escuelas, clínicas y se amplió el sistema de agua potable y electricidad.
                            Durante la segunda mitad del siglo XX, también creció la participación de Escuinapa en la vida política y cultural del estado.</p>
                    </div>
