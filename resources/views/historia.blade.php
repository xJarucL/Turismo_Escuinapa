                <div class="mb-10 grid grid-cols-1 md:grid-cols-2 gap-8 items-center">
                    <div>
                        <h2 class="text-2xl font-serif mb-4 border-b pb-2">1. Época prehispánica</h2>
                        <p class="font-serif mb-4 text-justify">Antes de la llegada de los españoles, Escuinapa formaba parte de un territorio ocupado por grupos indígenas pertenecientes a las culturas del noroeste de México. Los principales grupos eran los acaxees, xiximes, y tepehuanes, aunque también hubo presencia nahua debido a migraciones. Estas comunidades vivían en forma seminómada, practicaban la agricultura básica, la pesca en ríos y lagunas, y la caza de fauna local.</p>
                        <p class="font-serif mb-4 text-justify">El nombre "Escuinapa" proviene del náhuatl “Itzcuinapa” (itzcuini = perro, apan = río), que se interpreta como "río de los perros" o "lugar donde abundan los perros". Esto indica que ya existía una referencia importante al agua y a la fauna desde tiempos antiguos.</p>
